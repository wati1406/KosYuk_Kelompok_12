<h3>Panduan Menggunakan KosYuk</h3>
<p>Ikuti 4 langkah mudah berikut untuk menemukan kos impian Anda:</p>

<div class="guide-steps">
    <div class="guide-step">
        <div class="guide-content">
            <h4>1. Pilih Kriteria</h4>
            <p>Gunakan fitur pencarian dan filter untuk menemukan kos yang sesuai kebutuhan Anda. Anda dapat memilih: lokasi kos, harga sewa, fasilitas (WiFi, kamar mandi dalam, AC, parkir, dll.), dan tipe kos (putra/putri/campuran). Filter ini membantu menampilkan kos yang benar-benar cocok dengan keinginan Anda, sehingga pencarian lebih cepat dan efisien.</p>
        </div>
    </div>
    
    <div class="guide-step">
        <div class="guide-content">
            <h4>2. Lihat Detail Kos</h4>
            <p>Klik kos yang Anda pilih untuk melihat informasi lengkap, seperti: foto kamar dan lingkungan kos, fasilitas yang tersedia, harga sewa dan peraturan kos, lokasi di peta, dan ulasan atau penilaian dari penyewa lain. Tahap ini membantu Anda memastikan bahwa kos tersebut sesuai sebelum menghubungi pemilik.</p>
        </div>
    </div>
    
    <div class="guide-step">
        <div class="guide-content">
            <h4>3. Hubungi Pemilik Kos</h4>
            <p>Jika Anda tertarik, gunakan tombol WhatsApp atau Telepon yang tersedia. Melalui fitur ini Anda bisa: menanyakan ketersediaan kamar, memastikan fasilitas yang ditawarkan, berdiskusi tentang harga atau aturan kos, dan mengatur jadwal untuk survei lokasi. Kontak pemilik sudah diverifikasi dari data yang mereka berikan saat mendaftar.</p>
        </div>
    </div>
    
    <div class="guide-step">
        <div class="guide-content">
            <h4>4. Booking & Mulai Tinggal</h4>
            <p>Jika sudah cocok, Anda dapat melakukan booking sesuai arahan pemilik kos. Biasanya prosesnya meliputi: mengisi data diri, membayar uang muka atau pelunasan (sesuai aturan pemilik kos), konfirmasi bahwa kamar siap ditempati. Setelah semua selesai, Anda tinggal membawa barang dan mulai menempati kos pilihan Anda.</p>
        </div>
    </div>
</div>

